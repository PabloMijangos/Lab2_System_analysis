    private void figActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_figActionPerformed
        // TODO add your handling code here:
         figura = fig.getSelectedItem().toString();
        if(figura.equals("Cuadrado")){
            //figure=0;
            lbl_cuadrado.setText("Ingrese el lado del cuadrado");
            txt_cuadrado.setVisible(true);
            lbl_triangulo.setVisible(false);
            txt_triangulo.setVisible(false);          
        }
          if(figura.equals("Circulo")){
            //figure=0;
            lbl_cuadrado.setText("Ingrese Radio");
            txt_cuadrado.setVisible(true);
            lbl_triangulo.setVisible(false);
            txt_triangulo.setVisible(false);
       
        }
          if(figura.equals("Triangulo")){
            //figure=0;
            lbl_triangulo.setVisible(true);
            lbl_cuadrado.setText("Ingrese la base");
            txt_cuadrado.setVisible(true);     
            txt_triangulo.setVisible(true); 
        }
          if(figura.equals("Rectangulo")){
            //figure=0;
            lbl_triangulo.setVisible(true);
            lbl_cuadrado.setText("Ingrese la base");
            txt_cuadrado.setVisible(true);     
            txt_triangulo.setVisible(true); 
        }
        
     
    }//GEN-LAST:event_figActionPerformed

