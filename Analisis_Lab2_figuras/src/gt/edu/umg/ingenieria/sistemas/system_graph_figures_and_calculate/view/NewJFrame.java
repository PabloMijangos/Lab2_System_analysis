    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            // TODO add your handling code here:
        switch(figura){
            case "Circulo":
                    FiguraCirculoService circulo = new FiguraCirculoService() ;                
                    circulo.CrearFiguraCirculo(Integer.parseInt(txt_cuadrado.getText()),colorfig.getSelectedItem().toString(),bordefig.getSelectedItem().toString());
                    circulo.getdrawFigure(jPanel2.getGraphics());
                    jTextField3.setText(Float.toString(circulo.getPerimetrofiguras()));
                    jTextField4.setText(Float.toString(circulo.getAreafiguras()));
            break;
            case "Cuadrado":
                    FiguraCuadradoService cuadrado = new FiguraCuadradoService();
                    cuadrado.CrearFiguraCuadrado(Integer.parseInt(txt_cuadrado.getText()),colorfig.getSelectedItem().toString(),bordefig.getSelectedItem().toString());
                    cuadrado.getdrawFigure(jPanel2.getGraphics());
                    jTextField3.setText(Float.toString(cuadrado.getPerimetrofiguras()));
                    jTextField4.setText(Float.toString(cuadrado.getAreafiguras()));
                   
            break;
            case "Rectangulo":
                   FiguraRectanguloService rectangulo = new FiguraRectanguloService();
                   rectangulo.CrearFiguraRectangulo(Integer.parseInt(txt_cuadrado.getText()),Integer.parseInt(txt_triangulo.getText()),colorfig.getSelectedItem().toString(),bordefig.getSelectedItem().toString());
                    rectangulo.getdrawFigure(jPanel2.getGraphics());
                    jTextField3.setText(Float.toString(rectangulo.getPerimetrofiguras()));
                    jTextField4.setText(Float.toString(rectangulo.getAreafiguras()));
                   
            break;
            case "Triangulo":
                 FiguraTrianguloService triangulo = new FiguraTrianguloService();
                   triangulo.CrearFiguraTriangulo(Integer.parseInt(txt_cuadrado.getText()),Integer.parseInt(txt_triangulo.getText()),colorfig.getSelectedItem().toString(),bordefig.getSelectedItem().toString());
                    triangulo.getdrawFigure(jPanel2.getGraphics());
                    jTextField3.setText(Float.toString(triangulo.getPerimetrofiguras()));
                    jTextField4.setText(Float.toString(triangulo.getAreafiguras()));
            break;
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      switch(figura){
            case "Circulo":
                    FiguraCirculoService circulo = new FiguraCirculoService() ;                
                    circulo.CrearFiguraCirculo(Integer.parseInt(txt_cuadrado.getText()),colorfig.getSelectedItem().toString(),bordefig.getSelectedItem().toString());
                    circulo.getimage(jComboBox1.getSelectedItem().toString());   
            break;
            case "Cuadrado":
                    FiguraCuadradoService cuadrado = new FiguraCuadradoService();              
                    cuadrado.CrearFiguraCuadrado(Integer.parseInt(txt_cuadrado.getText()),colorfig.getSelectedItem().toString(),bordefig.getSelectedItem().toString());
                    cuadrado.getimage(jComboBox1.getSelectedItem().toString());   
            break;
            case "Rectangulo":
                FiguraRectanguloService rectangulo = new FiguraRectanguloService();              
                 rectangulo.CrearFiguraRectangulo(Integer.parseInt(txt_cuadrado.getText()),Integer.parseInt(txt_triangulo.getText()),colorfig.getSelectedItem().toString(),bordefig.getSelectedItem().toString());
                    rectangulo.getimage(jComboBox1.getSelectedItem().toString()); 
            break;
            case "Triangulo":
                FiguraTrianguloService triangulo = new FiguraTrianguloService();
                 triangulo.CrearFiguraTriangulo(Integer.parseInt(txt_cuadrado.getText()),Integer.parseInt(txt_triangulo.getText()),colorfig.getSelectedItem().toString(),bordefig.getSelectedItem().toString());
                    triangulo.getimage(jComboBox1.getSelectedItem().toString()); 
            break;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

